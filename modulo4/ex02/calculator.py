#!usr/bin/python3

primeiro = float(input("Insira um numero: "))
segundo = float(input("Insira mais um numero: "))
print("Obrigado!")

print(f"{primeiro} + {segundo} = {primeiro+segundo}")
print(f"{primeiro} - {segundo} = {primeiro-segundo}")
print(f"{primeiro} / {segundo} = {primeiro/segundo}")
print(f"{primeiro} * {segundo} = {primeiro*segundo}")


